int start_DEMO(void *prog, void *var, void *stack) {

   // zero the memory used as var and stack space
   memset(var, 0, DEMO_VAR_SIZE);
   memset(stack, 0, DEMO_STACK_SIZE);

   // copy the Spin program to the local array, in case it is
   // currently stored in XMM RAM
   memcpy(prog, DEMO_array, DEMO_PROG_SIZE);

   // start the Spin program
   return _coginit_Spin(DEMO_array, var, stack, DEMO_PROG_OFF, DEMO_STACK_OFF);

}

