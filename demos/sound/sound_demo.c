void print_plugin_names() {
   int type;
   request_t *rqst;

   int i;
   for (i = 0; i < 8; i++) {
      type = REGISTERED_TYPE(i);
      rqst = REQUEST_BLOCK(i);
      t_printf("Cog %d (%x) Type = %s\n", i, (unsigned)rqst, _plugin_name(type)); 
   }
}

void main() {
   char c;
   int freq = 1000;

   print_plugin_names();

   t_printf("\nPress keys 1 to 4 to play sounds, or\nPress 0 to stop all sounds\n");

   while (1) {
      c = k_wait();
      switch (c) {
         case '0':
            StopSound(0);
            StopSound(1);
            StopSound(2);
            StopSound(3);
            break;
         case '1': 
            PlaySoundPCM(0, demo_sound, demo_sound + sizeof(demo_sound), 255); 
            break;
         case '2': 
            PlaySoundFM(1, SHAPE_SQUARE, freq, SAMPLE_RATE * 2.8, 200, 0x3579ADEF); 
            break;
         case '3': 
            PlaySoundFM(2, SHAPE_SQUARE, (freq*5)/4, SAMPLE_RATE * 2.9, 200, 0x3579ADEF); 
            break;
         case '4': 
            PlaySoundFM(3, SHAPE_SQUARE, (freq*6)/4, SAMPLE_RATE * 3.0, 200, 0x3579ADEF); 
            break;
      }
   }
}                                                                                 
